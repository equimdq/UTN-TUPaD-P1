nota = int(input("ingrese su nota: "))
if nota >= 6:
    print("Aprobado")# El programa solicita al usuario que ingrese una nota y luego verifica si es mayor o igual a 6.
else:
    print("Desaprobado")  # Si la nota es menor a 6 imprime por pantalla "desaprobado"
# El programa utiliza una estructura condicional if-else para determinar el resultado.